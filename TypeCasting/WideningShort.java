class WideningShort
{
	public static void main(String[] args)
	{
		short s=200;
		System.out.println(s);

		//byte b=300;
		//System.out.println(b);

		int i1=300;
		System.out.println(i1);

		long l1=500;
		System.out.println(l1);

		float f=200;
		System.out.println(f);

		double d=201;
		System.out.println(d);

		char ch=129;
		System.out.println(ch);
	}
}
