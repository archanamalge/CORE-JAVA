class WideningChar
{
	public static void main(String[] args)
	{
		char ch='a';
		System.out.println(ch);

		byte b='c';
		System.out.println(b);

		short s='y';
		System.out.println(s);

		int a='B';
		System.out.println(a);

		long l1='R';
		System.out.println(l1);

		//Float f='4';
		//System.out.println(f);

		double d='4';
		System.out.println(d);

		
	}
}