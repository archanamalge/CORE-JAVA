class WideningByte
{
	public static void main(String[] args)
	{
		byte b1=60;
		System.out.println(b1);

		short s=3;
		System.out.println(s);

		int i1=20;
		System.out.println(i1);

		long l1=100;
		System.out.println(l1);
		
		float f=89;
		System.out.println(f);

		double d= 97;
		System.out.println(d);

		char ch=88;
		System.out.println(ch);
				
	}
}